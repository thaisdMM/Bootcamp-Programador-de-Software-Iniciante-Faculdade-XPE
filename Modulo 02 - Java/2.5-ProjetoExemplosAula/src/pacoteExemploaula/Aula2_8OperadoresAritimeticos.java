package pacoteExemploString;

public class Aula2_8OperadoresAritimeticos {

	public static void main(String[] args) {
		
		
		int n1 = 0 , n2 = 0, total = 0;
		
		//ADICAO
		n1 = 10;
		n2 = 20;
		
		total = n1 + n2;
		System.out.printf("O resultado de %d + %d = %d", n1, n2, total);
		
		//SUBTRACAO
		n1 = 50;
		n2 = 80;
		total = n1 - n2;
		System.out.printf("\nO resultado de %d - %d = %d", n1, n2, total);
	
		//MULTIPLICACAO
		total = n1 + n2;
		System.out.printf("O resultado de %d + %d = %d", n1, n2, total);
	
		
		
		
		
	}

}
